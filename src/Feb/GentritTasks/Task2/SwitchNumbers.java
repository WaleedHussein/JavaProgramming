package Feb.GentritTasks.Task2;

public class SwitchNumbers {
    public static void main(String[] args) {

        //- Swap two variable' values without using a third variable

        int n1 = 5, n2 = 7;

        n1 = n1 * n2; // 5 * 7
        n2 = n1 / n2; // n2 = (5 * 7) / 7 = 5
        n1 = n1 / n2; // n1 = (5*7) / 5 = 7

        System.out.println("n1 = " + n1);
        System.out.println("n2 = " + n2);







    }
}
