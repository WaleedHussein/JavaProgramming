package Nov.day07_Operators;

public class ArithmaticOperators {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 2;
        int div = num1/num2;
        int remainder = num1%num2;// remainder is 10-(3*3) = 1

        System.out.println(num1 + " divided by "+ num2 + " is equal to " + div + " with a remainder of " + remainder);

    }
}
