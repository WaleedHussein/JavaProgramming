package Nov.day04_Variable;

public class Square {
    public static void main(String[] args) {
        double side = 3.4;
        double area = side * side;
        double perimeter = side * 4;
        System.out.println("side = " + side);
        System.out.println("perimeter = " + perimeter);
        System.out.println("area = " + area);
    }
}
